class ItemDaNota {
    constructor(name, valor) {
        this.name = name;
        this.valor = valor;
    }
    getName() {
        return this.name;
    }
    setName(name) {
        this.name = name;
    }
    getValor() {
        return this.valor;
    }
    setvalor(valor) {
        this.valor = valor;
    }
}
